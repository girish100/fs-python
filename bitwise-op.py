#bitwise left shift operator
print("\n=======bitwise left shift operator=======")
x1 = 20
x2 = x1 << 2
print("X1, ls binary of x1",x1,bin(x1))
print("X2, ls binary of x2",x2,bin(x2),"\n")
#bitwise right shift operator
print("=======bitwise right shift operator=======")
x1 = 211
x2 = x1 >>3
print("X1, rs binary of x1",x1,bin(x1))
print("X2, rs binary of x2",x2,bin(x2))
#bitwise and,or,elclusive or op
print("\n=======bitwise and,or,elclusive or op=======")
a = 15
b = 19
d = a & b
print("bitwise and op of 15,19: ",d)
print("bitwise and op",x1&x2)
print("bitwise or",x1/x2)
print("bitwise xor",20^22)
#bitwise not
print("\n=======bitwise not operator=======")
a = 150
b = -110
print("a, bitwise not a: ",a,~a)
print("b, bitwise not b:",b,~b)
